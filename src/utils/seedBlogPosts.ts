export const seedBlogPosts = async () => {
  const API_URL = 'https://functions.poehali.dev/e92433da-3db2-4e99-b9d6-a4596b987e6a';
  const results = [];

  for (const post of blogPostsData) {
    try {
      const response = await fetch(API_URL, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(post)
      });

      const data = await response.json();
      
      if (response.ok) {
        results.push({ success: true, title: post.title, id: data.id });
        console.log(`✅ Статья "${post.title}" создана с ID: ${data.id}`);
      } else {
        results.push({ success: false, title: post.title, error: data.error });
        console.error(`❌ Ошибка при создании "${post.title}":`, data.error);
      }
    } catch (error) {
      results.push({ success: false, title: post.title, error: String(error) });
      console.error(`❌ Ошибка при создании "${post.title}":`, error);
    }
  }

  return results;
};